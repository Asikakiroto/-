#include <stdio.h>
int main()
{
    float a = 0.00000;
    float b = 0.00000;
    float c = 0.00000;
    scanf("%f%f%f", &a, &b, &c);
    float m = (a + b + c) / 3;//平均数公式
    float n = ((a - m) * (a - m) + (b - m) * (b - m) + (c - m) * (c - m)) / 3;//方差公式
    printf("平均数:%f\n", m);
    printf("方差：%f\n", n);
}